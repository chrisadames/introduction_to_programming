# useful_loop.rb

i = 0
loop do 
  i += 1
  puts i # puts 1
  break # this will cause execution to exit the loop
end

# break: keyword allows us to exit a loop at any point, so any code after a 
# break will not be executed. Note, break will not exit the program, only 
# exits the loop and execution will continue on from after the loop.

